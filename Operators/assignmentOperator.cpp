#include<iostream>
using namespace std;
int main(){
  
  int num1 = 56;
  int num2 = 48;
  int num3 = 78;
  int num4 = 36;
  
  num1 += 10; // num1 = num1 + 10
  num2 -= 8;  // num2 = num2 - 8
  num3 /= 14; // num3 = num3 / 14
  num4 %= 12; // num4 = num4 % 12
  
  cout<<num1<<endl;
  cout<<num2<<endl;
  cout<<num3<<endl;
  cout<<num4<<endl;
 
  return 0;
}